# –£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä—ã—Ö –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤
docker rm -f kupipodariday-database kupipodariday-backend kupipodariday-frontend 2>/dev/null || true

