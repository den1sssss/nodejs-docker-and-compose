if [ "$FRONTEND_STATUS" = "200" ]; then
    echo "‚úÖ –§—Ä–æ–Ω—Ç–µ–Ω–¥ —Ä–∞–±–æ—Ç–∞–µ—Ç (HTTP $FRONTEND_STATUS)"
else
    echo "‚ö†Ô∏è  –§—Ä–æ–Ω—Ç–µ–Ω–¥ –º–æ–∂–µ—Ç –±—ã—Ç—å –µ—â–µ –Ω–µ –≥–æ—Ç–æ–≤ (HTTP $FRONTEND_STATUS)"
